// Define a type alias for the employee object
type Employee = {
    id: number,
    name: string
};

// Declare an employee object using the defined type alias
let employee: Employee = {
    id: 1,
    name: 'sandesha'
};

// Modify the 'id' property of the 'employee' object
employee.id = 2;

// Modify the 'name' property of the 'employee' object
employee.name = 'joe';
